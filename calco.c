# include<stdio.h>

int main() {
    int number1, number2;
    number1 = 100;
    number2 = 2;
    printf("100 + 2 = %d\n", number1 + number2);
    printf("100 - 2 = %d\n", number1 - number2);
    printf("100 * 2 = %d\n", number1 * number2);
    printf("100 / 2 = %d\n", number1 / number2);
    return 0;
}